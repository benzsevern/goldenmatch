class TestDetectHeaderRow:
    def test_header_on_row_0(self):
        lines = ["name,age,city", "John,30,NYC", "Jane,25,Boston"]
        idx, has = detect_header_row(lines, ",")
        assert idx == 0
        assert has is True

    def test_header_on_row_3(self):
        lines = [
            "Report generated 2024-01-01",
            "---",
            "",
            "name,age,city",
            "John,30,NYC",
        ]
        idx, has = detect_header_row(lines, ",")
        assert idx == 3
        assert has is True

    def test_no_header(self):
        lines = ["1,2,3", "4,5,6", "7,8,9"]
        idx, has = detect_header_row(lines, ",")
        assert has is False


