# ---------------------------------------------------------------------------
# Encoding Detection
# ---------------------------------------------------------------------------


class TestDetectEncoding:
    def test_utf8(self, tmp_path):
        p = tmp_path / "utf8.txt"
        p.write_text("hello world\n", encoding="utf-8")
        assert detect_encoding(p) == "utf8"

    def test_utf8_bom(self, tmp_path):
        p = tmp_path / "bom.txt"
        p.write_bytes(b"\xef\xbb\xbfhello world\n")
        assert detect_encoding(p) == "utf8"

    def test_latin1(self, tmp_path):
        p = tmp_path / "latin1.txt"
        # Write bytes that are valid latin-1 but invalid utf-8
        p.write_bytes(b"caf\xe9\n")
        enc = detect_encoding(p)
        # Should still return utf8 (our fallback) since latin-1 without
        # mojibake patterns is ambiguous
        assert enc == "utf8"

    def test_utf16_le_bom(self, tmp_path):
        p = tmp_path / "utf16.txt"
        p.write_bytes(b"\xff\xfeh\x00i\x00")
        assert detect_encoding(p) == "utf-16"


