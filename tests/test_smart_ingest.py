class TestKeyValue:
    def test_detect_colon(self):
        lines = [
            "Name: John Smith",
            "Age: 30",
            "City: New York",
        ]
        result = detect_key_value(lines)
        assert result is not None
        sep, keys = result
        assert sep == ":"
        assert "Name" in keys
        assert "Age" in keys

    def test_detect_equals(self):
        lines = [
            "name=John",
            "age=30",
            "city=NY",
        ]
        result = detect_key_value(lines)
        assert result is not None
        sep, keys = result
        assert sep == "="

    def test_parse_multiple_records(self):
        text = (
            "Name: John Smith\n"
            "Age: 30\n"
            "City: New York\n"
            "Name: Jane Doe\n"
            "Age: 25\n"
            "City: Boston\n"
        )
        df = parse_key_value(text, ":", ["Name", "Age", "City"])
        assert len(df) == 2
        assert "Name" in df.columns
        assert df["Name"][0] == "John Smith"
        assert df["Name"][1] == "Jane Doe"

    def test_detect_returns_none_for_csv(self):
        lines = ["a,b,c", "1,2,3"]
        assert detect_key_value(lines) is None


