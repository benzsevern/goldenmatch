class TestLoadFileIntegration:
    def test_auto_detect_csv(self, tmp_path):
        p = tmp_path / "data.csv"
        p.write_text("name,age\nJohn,30\nJane,25\n")
        lf = load_file(p)
        assert isinstance(lf, pl.LazyFrame)
        df = lf.collect()
        assert "name" in df.columns
        assert len(df) == 2

    def test_explicit_delimiter_still_works(self, tmp_path):
        p = tmp_path / "data.csv"
        df = pl.DataFrame({"a": [1, 2], "b": ["x", "y"]})
        df.write_csv(p, separator="\t")
        lf = load_file(p, delimiter="\t")
        result = lf.collect()
        assert result.columns == ["a", "b"]

    def test_txt_file_auto(self, tmp_path):
        p = tmp_path / "data.txt"
        p.write_text("name|age|city\nJohn|30|NYC\nJane|25|Boston\n")
        lf = load_file(p, parse_mode="auto")
        assert isinstance(lf, pl.LazyFrame)
        df = lf.collect()
        assert df.width >= 3

    def test_tsv_auto(self, tmp_path):
        p = tmp_path / "data.tsv"
        p.write_text("name\tage\nJohn\t30\nJane\t25\n")
        lf = load_file(p)
        df = lf.collect()
        assert "name" in df.columns

    def test_parquet_unchanged(self, sample_parquet):
        lf = load_file(sample_parquet)
        assert isinstance(lf, pl.LazyFrame)
        df = lf.collect()
        assert len(df) == 3

    def test_excel_unchanged(self, tmp_path):
        p = tmp_path / "test.xlsx"
        df = pl.DataFrame({"id": [1, 2], "name": ["A", "B"]})
        df.write_excel(p)
        lf = load_file(p)
        assert isinstance(lf, pl.LazyFrame)

    def test_backwards_compat_positional_delimiter(self, tmp_path):
        """Existing code passing delimiter explicitly should still work."""
        p = tmp_path / "data.csv"
        df = pl.DataFrame({"x": [1], "y": [2]})
        df.write_csv(p, separator=",")
        lf = load_file(p, delimiter=",")
        result = lf.collect()
        assert result.columns == ["x", "y"]
