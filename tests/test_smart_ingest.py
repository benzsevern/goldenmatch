# Delimiter Sniffing
# ---------------------------------------------------------------------------


class TestDetectDelimiter:
    def test_comma(self):
        lines = ["a,b,c", "1,2,3", "4,5,6"]
        assert detect_delimiter(lines) == ","

    def test_tab(self):
        lines = ["a\tb\tc", "1\t2\t3", "4\t5\t6"]
        assert detect_delimiter(lines) == "\t"

    def test_pipe(self):
        lines = ["a|b|c", "1|2|3", "4|5|6"]
        assert detect_delimiter(lines) == "|"

    def test_semicolon(self):
        lines = ["a;b;c", "1;2;3", "4;5;6"]
        assert detect_delimiter(lines) == ";"

    def test_mixed_picks_most_consistent(self):
        # Tabs appear consistently, commas appear in data values
        lines = [
            "name\tage\tcity",
            "John, Jr.\t30\tNew York, NY",
            "Jane\t25\tBoston, MA",
        ]
        assert detect_delimiter(lines) == "\t"

    def test_empty_lines_default(self):
        lines = ["", "", ""]
        assert detect_delimiter(lines) == ","

    def test_no_delimiters_default(self):
        lines = ["hello", "world", "test"]
        assert detect_delimiter(lines) == ","


