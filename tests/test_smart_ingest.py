class TestSmartLoad:
    def test_auto_csv(self, tmp_path):
        p = tmp_path / "data.csv"
        p.write_text("name,age,city\nJohn,30,NYC\nJane,25,Boston\n")
        df, meta = smart_load(p)
        assert df.width >= 3
        assert len(df) == 2
        assert meta["encoding"] == "utf8"
        assert "detection_log" in meta

    def test_auto_tsv(self, tmp_path):
        p = tmp_path / "data.tsv"
        p.write_text("name\tage\tcity\nJohn\t30\tNYC\nJane\t25\tBoston\n")
        df, meta = smart_load(p)
        assert df.width >= 3
        assert len(df) == 2

    def test_auto_pipe(self, tmp_path):
        p = tmp_path / "data.txt"
        p.write_text("name|age|city\nJohn|30|NYC\nJane|25|Boston\n")
        df, meta = smart_load(p)
        assert df.width >= 3

    def test_explicit_delimited(self, tmp_path):
        p = tmp_path / "data.txt"
        p.write_text("name;age;city\nJohn;30;NYC\n")
        df, meta = smart_load(p, parse_mode="delimited")
        assert meta["parse_mode"] == "delimited"
        assert df.width >= 3

    def test_explicit_key_value(self, tmp_path):
        p = tmp_path / "data.txt"
        p.write_text("Name: John\nAge: 30\nCity: NYC\nName: Jane\nAge: 25\nCity: Boston\n")
        df, meta = smart_load(p, parse_mode="key_value")
        assert meta["parse_mode"] == "key_value"
        assert len(df) == 2

    def test_explicit_entity_extract(self, tmp_path):
        p = tmp_path / "notes.txt"
        p.write_text("Email john@example.com phone 267-555-1234\n")
        df, meta = smart_load(p, parse_mode="entity_extract")
        assert meta["parse_mode"] == "entity_extract"

    def test_metadata_dict(self, tmp_path):
        p = tmp_path / "data.csv"
        p.write_text("a,b\n1,2\n")
        _, meta = smart_load(p)
        assert "encoding" in meta
        assert "delimiter" in meta
        assert "header_row" in meta
        assert "has_header" in meta
        assert "skip_rows" in meta
        assert "parse_mode" in meta
        assert "detection_log" in meta

    def test_auto_kv_file(self, tmp_path):
        p = tmp_path / "data.txt"
        content = "Name: John\nAge: 30\nCity: NYC\nName: Jane\nAge: 25\nCity: Boston\n"
        p.write_text(content)
        df, meta = smart_load(p)
        assert len(df) >= 2

    def test_auto_block_file(self, tmp_path):
        p = tmp_path / "data.txt"
        content = "John Smith\n30\nNew York\n\nJane Doe\n25\nBoston\n"
        p.write_text(content)
        df, meta = smart_load(p)
        assert len(df) >= 2

    def test_messy_file_with_junk_header(self, tmp_path):
        p = tmp_path / "messy.csv"
        content = (
            "Report generated 2024-01-01\n"
            "---\n"
            "name,age,city\n"
            "John,30,NYC\n"
            "Jane,25,Boston\n"
            "END OF FILE\n"
        )
        p.write_text(content)
        df, meta = smart_load(p)
        # Should detect header and skip junk
        assert df.width >= 2
        assert len(df) >= 2


