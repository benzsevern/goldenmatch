class TestFixedWidth:
    def test_detect_fixed_width_columns(self):
        lines = [
            "Name       Age  City      ",
            "John        30  New York  ",
            "Jane        25  Boston    ",
            "Bob         40  Chicago   ",
        ]
        boundaries = detect_fixed_width(lines)
        assert boundaries is not None
        assert len(boundaries) >= 3

    def test_parse_fixed_width_with_header(self):
        lines = [
            "Name       Age  City      ",
            "John        30  New York  ",
            "Jane        25  Boston    ",
        ]
        boundaries = detect_fixed_width(lines)
        assert boundaries is not None
        df = parse_fixed_width(lines, boundaries, has_header=True)
        assert len(df) == 2
        assert df.columns[0] == "Name"

    def test_parse_fixed_width_without_header(self):
        lines = [
            "John        30  New York  ",
            "Jane        25  Boston    ",
            "Bob         40  Chicago   ",
            "Alice       35  Denver    ",
        ]
        boundaries = detect_fixed_width(lines)
        assert boundaries is not None
        df = parse_fixed_width(lines, boundaries, has_header=False)
        assert len(df) == 4
        assert df.columns[0] == "column_1"

    def test_detect_returns_none_for_delimited(self):
        lines = ["a,b,c", "1,2,3", "4,5,6"]
        result = detect_fixed_width(lines)
        # Could be None or not — just verify no crash
        assert result is None or isinstance(result, list)


