class TestEntityExtraction:
    def test_email(self):
        text = "Contact john@example.com for info.\nOr jane@test.com."
        df = extract_entities(text)
        assert "email" in df.columns
        emails = df["email"].to_list()
        assert "john@example.com" in emails

    def test_phone(self):
        text = "Call 267-555-1234 or (212) 555-9999."
        df = extract_entities(text)
        assert "phone" in df.columns

    def test_zip(self):
        text = "Shipping to 19382 or 90210-1234."
        df = extract_entities(text)
        assert "zip" in df.columns
        zips = df["zip"].to_list()
        assert any("19382" in z for z in zips)

    def test_name_extraction(self):
        text = "Contact John Smith at john@example.com."
        df = extract_entities(text)
        assert "name" in df.columns or "email" in df.columns

    def test_empty_text(self):
        df = extract_entities("")
        assert len(df) == 0


