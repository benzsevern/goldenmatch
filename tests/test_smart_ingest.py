class TestDetectSkipRows:
    def test_junk_before_header(self):
        lines = [
            "Generated by System v2.1",
            "---",
            "name,age",
            "John,30",
        ]
        skips = detect_skip_rows(lines, ",", header_row=2)
        assert 0 in skips
        assert 1 in skips
        assert 2 not in skips
        assert 3 not in skips

    def test_separator_lines(self):
        lines = [
            "name,age",
            "--------",
            "John,30",
        ]
        skips = detect_skip_rows(lines, ",", header_row=0)
        assert 1 in skips

    def test_end_of_file(self):
        lines = [
            "name,age",
            "John,30",
            "END OF FILE",
        ]
        skips = detect_skip_rows(lines, ",", header_row=0)
        assert 2 in skips

    def test_empty_lines(self):
        lines = ["name,age", "", "John,30", ""]
        skips = detect_skip_rows(lines, ",", header_row=0)
        assert 1 in skips
        assert 3 in skips


